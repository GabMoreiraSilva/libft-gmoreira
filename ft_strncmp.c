#include "libft.h"

int	ft_strncmp(const char *str1, const char *str2, size_t num)
{
	while (str1 && str2 && num)
	{
		if (*(unsigned char *)str1 != *(unsigned char *)str2)
			return (*(unsigned char *)str1 - *(unsigned char *)str2);
		str1++;
		str2++;
		num--;
	}
	return (0);
}
